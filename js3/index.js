let musicalGroup = 4;
if (musicalGroup <= 0) {
  console.log("not a group");
} else if (musicalGroup == 1) {
  console.log("solo");
} else if (musicalGroup == 2) {
  console.log("duet");
} else if (musicalGroup == 3) {
  console.log("trio");
} else if (musicalGroup == 4) {
  console.log("quartet");
} else {
  console.log("This is a large group");
}
